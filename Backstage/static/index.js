$('#clear').click(function(){
    $('#consume_type').val('');
    $('#order_id').val('');
    $('#consume_uin').val('');
    $('#accept_uin').val('');
    $('#payment_id').val('');
    $('#fee').val('');
    $('#money').val('');
    $('#before_money').val('');
    $('#after_money').val('');
});
$('#submit').click(function(){
    $.ajax({
        url:"/index/",
        type:"POST",
        data:{
            'tbl':'dw_money_flow',
            'consume_type':$('#consume_type').val(),
            'order_id':$('#order_id').val(),
            'consume_uin':$('#consume_uin').val(),
            'accept_uin':$('#accept_uin').val(),
            'payment_id':$('#payment_id').val(),
            'fee':$('#fee').val(),
            'money':$('#money').val(),
            'before_money':$('#before_money').val(),
            'after_money':$('#after_money').val()
        },
        success:function(a){
            if(a=='OK'){
                alert(a)
            }else{
                alert(a)
            }
        }
    })
});
function display_hide_left(hide_div_left_id){
    var hide_div_left_x = document.getElementById(hide_div_left_id);  //根据id获取dom
    if ( hide_div_left_x.classList.length == 1){
        hide_div_left_x.classList.remove("hide_windows")}
    else{
        hide_div_left_x.classList.add("hide_windows")
    }
}
